import mongoose from "mongoose";

export const PersonSchema = new mongoose.Schema({
    personId: {
        type: String,

    },
    userName: {
        type: String,
        required: true
    },
    fullName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    type: {
        type: String,
        required: true
    }


})